function Window_OnUnload()
{

	if(hInternalIU!=null && !hInternalIU.closed)
		hInternalIU.close();
	if(hImportIU!=null && !hImportIU.closed)
		hImportIU.close();
	if(hExportIU!=null && !hExportIU.closed)
		hExportIU.close();
	return
}
